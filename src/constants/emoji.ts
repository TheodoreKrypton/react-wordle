type Hints = {
  [key: string]: string[];
}


export const hints : Hints = {}
Object.keys(emoji).forEach(key => {
  const v = emoji[key];
  if (v === '') {
    return;
  }
  if (hints[v]) {
    hints[v] = [...hints[v], key]
  } else {
    hints[v] = [key]
  }
});

export const wordToEmoji = (word: string) => {
  return word.split('').map(char => emoji[pinyin[char]]).join(',');
}